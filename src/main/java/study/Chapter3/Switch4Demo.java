package study.Chapter3;

public class Switch4Demo {
    public static void main(String[] args) {
        int number = 3;
        switch (number) {
            case 4:
                System.out.println("*");
            case 3:
                System.out.println("*");
            case 2:
                System.out.println("*");
            case 1:
                System.out.printf("*");
        }
    }
}
